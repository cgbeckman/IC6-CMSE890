def my_adder(a : float | int, b : float | int, c : float | int)-> float | int:
    """
    This function will sum 3 numbers according to the following equation: integer 1 + integer 2 + integer 3 = y
    
    Args:
         a: integer 1 
         b: integer 2
         c: integer 3
    Returns:
            y, the sum of integers 1-3.
    Authors:
            Made by Andrew Fullard
            Edited by Cate Beckman for CMSE 890
    """

    # this is the summation
    out = a + b + c

    return out

